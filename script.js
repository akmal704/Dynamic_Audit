function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('da-lang', lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = T[lang]?.[key];
    if (val !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    const val = T[lang]?.[key];
    if (val !== undefined) el.placeholder = val;
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  /* language buttons */
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
  });

  /* hamburger */
  const hamburger = document.getElementById('hamburger');
  const navMenu   = document.getElementById('nav-menu');
  if (hamburger && navMenu) {
    hamburger.addEventListener('click', () => {
      hamburger.classList.toggle('open');
      navMenu.classList.toggle('open');
    });
    navMenu.querySelectorAll('a').forEach(a => {
      a.addEventListener('click', () => {
        hamburger.classList.remove('open');
        navMenu.classList.remove('open');
      });
    });
  }

  /* sticky nav */
  const nav = document.getElementById('site-nav');
  window.addEventListener('scroll', () => {
    nav?.classList.toggle('scrolled', window.scrollY > 40);
  });

  /* scroll reveal */
  const observer = new IntersectionObserver(entries => {
    entries.forEach(e => { if (e.isIntersecting) { e.target.classList.add('visible'); } });
  }, { threshold: 0.12 });
  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

  /* apply saved language */
  applyLang(currentLang);

  /* form submit handlers */
  handleFormSubmit('form-general', 'https://formspree.io/f/mzdqrkne');
  handleFormSubmit('form-audit',   'https://formspree.io/f/xbdeopnq');
});

/* ── Contact tabs ──────────────────────────────────────────── */
function switchTab(tabId) {
  document.querySelectorAll('.contact-tab-btn').forEach(b => b.classList.toggle('active', b.dataset.tab === tabId));
  document.querySelectorAll('.contact-tab-pane').forEach(p => p.classList.toggle('active', p.id === tabId));
}

/* ── Lightbox ──────────────────────────────────────────────── */
function openLightbox(src, alt) {
  const lb = document.getElementById('lightbox');
  if (!lb) return;
  lb.querySelector('img').src  = src;
  lb.querySelector('img').alt  = alt || '';
  lb.classList.add('open');
  document.body.style.overflow = 'hidden';
}
function closeLightbox() {
  const lb = document.getElementById('lightbox');
  if (!lb) return;
  lb.classList.remove('open');
  document.body.style.overflow = '';
}

/* ── Form submit helpers ───────────────────────────────────── */
function handleFormSubmit(formId, endpoint) {
  const form = document.getElementById(formId);
  if (!form) return;
  form.addEventListener('submit', async e => {
    e.preventDefault();
    const btn = form.querySelector('[type="submit"]');
    const original = btn.innerHTML;
    btn.disabled = true;
    btn.innerHTML = '…';
    try {
      const res = await fetch(endpoint, {
        method: 'POST',
        body: new FormData(form),
        headers: { 'Accept': 'application/json' }
      });
      if (res.ok) {
        btn.innerHTML = '✓ Sent';
        form.reset();
        setTimeout(() => { btn.disabled = false; btn.innerHTML = original; }, 3500);
      } else { throw new Error(); }
    } catch {
      btn.innerHTML = 'Error — try again';
      setTimeout(() => { btn.disabled = false; btn.innerHTML = original; }, 3500);
    }
  });
}
